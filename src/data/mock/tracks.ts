  ['Jan 25','Feb 25','Mar 25','Apr 25','May 25','Jun 25'].map((month, i) => ({
    month,
    streams: Math.round(base + Math.sin(i) * variance + i * (variance / 4)),
    revenue: parseFloat((((base + Math.sin(i) * variance + i * (variance / 4)) / 1000) * 3.5).toFixed(2)),
  }));

